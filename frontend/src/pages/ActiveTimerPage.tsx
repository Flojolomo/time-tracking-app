import React, { useEffect } from 'react';
import { useActiveTimer } from '../hooks/useActiveTimer';
import { TimerWidget } from '../components/TimerWidget';

export const ActiveTimerPage: React.FC = () => {
  const { activeRecord, isLoading, error, loadActiveRecord, updateActiveRecord, clearError } = useActiveTimer();

  useEffect(() => {
    loadActiveRecord();
  }, [loadActiveRecord]);

  if (isLoading) {
    return (
      <div className="flex items-center justify-center min-h-96">
        <div className="text-center">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-indigo-600 mx-auto"></div>
          <p className="mt-4 text-gray-600">Loading active timer...</p>
        </div>
      </div>
    );
  }

  if (error) {
    return (
      <div className="bg-red-50 border border-red-200 rounded-md p-6">
        <div className="flex">
          <div className="flex-shrink-0">
            <svg className="h-5 w-5 text-red-400" viewBox="0 0 20 20" fill="currentColor">
              <path fillRule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z" clipRule="evenodd" />
            </svg>
          </div>
          <div className="ml-3">
            <h3 className="text-sm font-medium text-red-800">Error loading active timer</h3>
            <p className="mt-1 text-sm text-red-700">{error}</p>
            <button
              onClick={() => {
                clearError();
                loadActiveRecord();
              }}
              className="mt-2 text-sm text-red-800 underline hover:text-red-900"
            >
              Try again
            </button>
          </div>
        </div>
      </div>
    );
  }

  return (
    <TimerWidget 
      activeRecord={activeRecord} 
      onRecordChange={loadActiveRecord}
      onRecordUpdate={updateActiveRecord}
    />
  );
};